CREATE TABLE Users (
    PersonID int,
    Firstname varchar(255),
    Lastname varchar(255),
    BirthDate varchar(255),
    City varchar(255),
	Email varchar(255),
	PersonDescript varchar(255)
);

CREATE TABLE Adverts (
	AdvertID int,
	Brand varchar(255),
	Model varchar(255),
	Engine varchar(255),
	DateOfProduction varchar(255),
	Milage varchar(255),
	DateOfRegistrationInPoland varchar(255),
	AdvertName varchar(255),
	AdvertsDescript varchar(255)
)
